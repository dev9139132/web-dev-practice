let num = 5;

console.log(num.toString()); // "5"
console.log(num.toString(2)); // "101" (binary representation)
console.log(num.toString(8)); // "5" (octal representation)
console.log(num.toString(10)); // "5" (decimal representation)
console.log(num.toString(16)); // "5" (hexadecimal representation)